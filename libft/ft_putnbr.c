#include "libft.h"

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		nb = -214748364;
		ft_putnbr(nb);
		ft_putchar('8');
		return ;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar(nb + '0');
}
